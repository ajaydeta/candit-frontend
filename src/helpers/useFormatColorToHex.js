export default function useFormatColorToHex(color) {
    switch (color) {
        case "primary":
            return  "#24305e";
        case "orange":
            return  "#f76d6d";
        case "dark-blue":
            return  "#374785";
        case "tosca":
            return  "#a8d1e7";
        case "yellow":
            return  "#f7e9a0";
        case "gray":
            return  "#d0d0d0";
        case "white":
            return  "#ffffff";
    }
}